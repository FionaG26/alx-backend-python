#!/usr/bin/env python3

"""
Module: 1-concat
Description: Contains a function to concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result as a new string.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenation of str1 and str2.
    """
    return str1 + str2
