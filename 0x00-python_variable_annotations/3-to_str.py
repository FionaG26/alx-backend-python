#!/usr/bin/env python3

"""
Module: 3-to_str
Description: Contains a function to convert a float to its string
representation.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Args:
        n (float): The float number.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
