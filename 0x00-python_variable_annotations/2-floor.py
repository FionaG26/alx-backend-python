#!/usr/bin/env python3

"""
Module: 2-floor
Description: Contains a function to calculate the floor value of a float.
"""

import math


def floor(n: float) -> int:
    """
    Calculates the floor value of a float and returns it as an integer.

    Args:
        n (float): The float number.

    Returns:
        int: The floor value of the float.
    """
    return math.floor(n)
